package ni.generator.cpp;

import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;
import java.util.logging.Level;
import java.util.logging.Logger;

/**
 *
 * @author Samuel Audet
 */
public class Piper extends Thread {
    public Piper(InputStream is, OutputStream os) {
        this.is = is;
        this.os = os;
    }

    private static final Logger logger = Logger.getLogger(Piper.class.getName());

    private InputStream is;
    private OutputStream os;

    @Override public void run() {
        try {
            byte[] buffer = new byte[1024];
            int length;
            while ((length = is.read(buffer)) > 0) {
                os.write(buffer, 0, length);
            }
        } catch (IOException e) {
            logger.log(Level.SEVERE, "Could not pipe from the InputStream to the OutputStream.", e);
        }
    }
}
